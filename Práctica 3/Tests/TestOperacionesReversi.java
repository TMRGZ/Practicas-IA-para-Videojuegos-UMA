    @Test
    public void testDatos() {
        probarDatos(datos);
    }

    /**
     * Recibe un array Object[][] con los datos de un conjunto
     * de estados.
     * Para cada estado obtiene los datos, lo constuye, y
     * comprueba que los métodos de consulta (contenidoTablero,
     * turno1 y anteriorPasa) devuelven los valores correctos.
     */

    private void probarDatos(Object[][][] estados) {

        System.out.println("Probando datos ");

        for (int i = 0; i < estados.length; i++) {

            //Obtenemos los datos del estado a crear
            int nDatos = estados[i].length;

            int nFilas = nDatos - 2;   //número de filas del tablero

            int[][] tablero = new int[nFilas][];
            for (int k = 0; k < nFilas; k++) {
                tablero[k] = copiar(estados[i][k]);
            }
            Boolean turno1 = (Boolean) estados[i][nFilas][0];
            Boolean anteriorPasa = (Boolean) estados[i][nFilas + 1][0];

            //Creamos el estado
            Reversi e = new Reversi(tablero, turno1, anteriorPasa);

            System.out.println("Estado " + i);
            compruebaReversi(e, tablero, turno1, anteriorPasa);
        }//for i

    }


    /**
     * Comprueba que el estado e se corresponde con las partes recibidas (tablero, turno1, anteriorPasa).
     */
    private static void compruebaReversi(Reversi e, int[][] tablero, boolean turno1, boolean anteriorPasa) {
        //Comprobamos
        int nFilas = e.tablero().nFilas();
        int[][] eTab = e.contenidoTablero();

        Assert.assertEquals("número de filas del tablero ", nFilas, tablero.length);
        for (int k = 0; k < nFilas; k++) {
            Assert.assertArrayEquals("Tablero fila J1 ", tablero[k], eTab[k]);
        }

        Assert.assertEquals("turno1 ", turno1, e.turno1());
        Assert.assertEquals("anteriorPasa ", anteriorPasa, e.anteriorPasa());

    }


